#!/usr/bin/env node

import { spawn, spawnSync } from "node:child_process";
import { randomBytes } from "node:crypto";
import { existsSync, mkdirSync, readFileSync, writeFileSync, rmSync } from "node:fs";
import net from "node:net";
import os from "node:os";
import path from "node:path";
import { fileURLToPath } from "node:url";

const packageRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const appHome = process.env.AGENT_ORGANIZER_HOME || path.join(os.homedir(), ".agent-organizer");
const dataDir = process.env.AGENT_ORGANIZER_DATA_DIR || path.join(appHome, "data");
const envPath = path.join(appHome, ".env");
const pidPath = path.join(dataDir, "agent-organizer.pid");
const defaultRedisUrl = "redis://127.0.0.1:6379";

function printHelp() {
  console.log(`Agent Organizer

Usage:
  agent-organizer start    Install runtime state, ensure Redis, and start the app
  agent-organizer status   Check server and Redis status
  agent-organizer stop     Stop the foreground server started by this CLI
  agent-organizer doctor   Check local prerequisites

Environment:
  AGENT_ORGANIZER_HOME      Default: ~/.agent-organizer
  AGENT_ORGANIZER_DATA_DIR  Default: ~/.agent-organizer/data
  PORT                      Default: 8791
  REDIS_URL                 Default: redis://127.0.0.1:6379
`);
}

function fail(message) {
  console.error(`Error: ${message}`);
  process.exit(1);
}

function commandExists(command) {
  return spawnSync(command, ["--version"], { stdio: "ignore" }).status === 0
    || spawnSync("which", [command], { stdio: "ignore" }).status === 0;
}

function ensureNode() {
  const major = Number(process.versions.node.split(".")[0] || "0");
  if (major < 22) {
    fail(`Node.js >= 22 is required. Current: ${process.version}`);
  }
}

function parseEnvFile(filePath) {
  if (!existsSync(filePath)) return {};
  const out = {};
  for (const line of readFileSync(filePath, "utf8").split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const eq = trimmed.indexOf("=");
    if (eq === -1) continue;
    const key = trimmed.slice(0, eq).trim();
    let value = trimmed.slice(eq + 1).trim();
    if (
      (value.startsWith('"') && value.endsWith('"'))
      || (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    out[key] = value;
  }
  return out;
}

function writeEnvFile(values) {
  const lines = [
    "# Generated by `npx agent-organizer@latest start`",
    `PORT=${values.PORT}`,
    `NODE_ENV=${values.NODE_ENV}`,
    `SESSION_AUTH_TOKEN=${values.SESSION_AUTH_TOKEN}`,
    `DB_PATH=${values.DB_PATH}`,
    `REDIS_URL=${values.REDIS_URL}`,
    `REDIS_ENABLED=${values.REDIS_ENABLED}`,
    "AUTO_ASSIGN_TASK_ON_CREATE=true",
    "AUTO_RUN_TASK_ON_CREATE=true",
    "AUTO_DISPATCH_INTERVAL_MS=60000",
    "ORPHAN_AUTO_RESPAWN_MAX=3",
    "GITHUB_SYNC_ENABLED=false",
    "GITHUB_SYNC_REPO=",
    "GITHUB_SYNC_TOKEN=",
    "GITHUB_SYNC_PROJECT_PATH=",
    "GITHUB_SYNC_INTERVAL_MS=300000",
    "TELEGRAM_CONTROL_ENABLED=true",
    "TELEGRAM_BOT_TOKEN=",
    "TELEGRAM_CHAT_ID=",
    "",
  ];
  writeFileSync(envPath, lines.join("\n"), { mode: 0o600 });
}

function ensureRuntimeEnv() {
  mkdirSync(dataDir, { recursive: true });

  const existing = parseEnvFile(envPath);
  const values = {
    PORT: existing.PORT || process.env.PORT || "8791",
    NODE_ENV: existing.NODE_ENV || "production",
    SESSION_AUTH_TOKEN:
      existing.SESSION_AUTH_TOKEN
      || process.env.SESSION_AUTH_TOKEN
      || randomBytes(32).toString("hex"),
    DB_PATH:
      existing.DB_PATH
      || process.env.DB_PATH
      || path.join(dataDir, "agent-organizer.db"),
    REDIS_URL: existing.REDIS_URL || process.env.REDIS_URL || defaultRedisUrl,
    REDIS_ENABLED: existing.REDIS_ENABLED || process.env.REDIS_ENABLED || "true",
  };

  if (!existsSync(envPath)) {
    writeEnvFile(values);
    console.log(`Created ${envPath}`);
  }

  return { ...existing, ...values, ...process.env };
}

function redisTarget(redisUrl) {
  const parsed = new URL(redisUrl);
  return {
    host: parsed.hostname || "127.0.0.1",
    port: Number(parsed.port || "6379"),
  };
}

function isLocalRedisTarget({ host }) {
  return host === "127.0.0.1" || host === "localhost" || host === "::1";
}

function canConnect(host, port, timeoutMs = 800) {
  return new Promise((resolve) => {
    const socket = net.createConnection({ host, port });
    const done = (ok) => {
      socket.removeAllListeners();
      socket.destroy();
      resolve(ok);
    };
    socket.setTimeout(timeoutMs);
    socket.once("connect", () => done(true));
    socket.once("timeout", () => done(false));
    socket.once("error", () => done(false));
  });
}

function run(command, args) {
  const result = spawnSync(command, args, { encoding: "utf8", stdio: ["ignore", "pipe", "pipe"] });
  return result;
}

async function ensureRedis(env) {
  if (env.REDIS_ENABLED === "false") {
    console.log("Redis disabled by REDIS_ENABLED=false");
    return;
  }

  const target = redisTarget(env.REDIS_URL || defaultRedisUrl);
  if (await canConnect(target.host, target.port)) {
    console.log(`Redis ready at ${env.REDIS_URL}`);
    return;
  }

  if (!isLocalRedisTarget(target)) {
    fail(`Redis is not reachable at ${env.REDIS_URL}`);
  }

  if (!commandExists("docker")) {
    fail("Redis is required. Install Docker or start Redis locally on 127.0.0.1:6379.");
  }

  const name = "agent-organizer-redis";
  const inspect = run("docker", ["container", "inspect", name]);
  if (inspect.status === 0) {
    const started = run("docker", ["start", name]);
    if (started.status !== 0) fail(`Failed to start Redis container ${name}: ${started.stderr.trim()}`);
  } else {
    const created = run("docker", [
      "run",
      "-d",
      "--name",
      name,
      "-p",
      `${target.port}:6379`,
      "redis:7-alpine",
    ]);
    if (created.status !== 0) fail(`Failed to create Redis container ${name}: ${created.stderr.trim()}`);
  }

  for (let i = 0; i < 30; i += 1) {
    if (await canConnect(target.host, target.port, 500)) {
      console.log(`Redis ready in Docker container: ${name}`);
      return;
    }
    await new Promise((resolve) => setTimeout(resolve, 500));
  }

  fail(`Redis container started but ${env.REDIS_URL} is still unreachable`);
}

async function health(port) {
  try {
    const res = await fetch(`http://127.0.0.1:${port}/api/health`);
    return res.ok ? await res.json() : null;
  } catch {
    return null;
  }
}

async function start() {
  ensureNode();
  const env = ensureRuntimeEnv();
  const port = Number(env.PORT || "8791");
  const running = await health(port);
  if (running) {
    console.log(`Agent Organizer is already running: http://localhost:${port}`);
    return;
  }

  if (!existsSync(path.join(packageRoot, "dist", "index.html"))) {
    fail("Missing dist/index.html. The npm package must be built before publishing.");
  }

  await ensureRedis(env);

  const serverEntry = path.join(packageRoot, "server", "index.ts");
  console.log(`Starting Agent Organizer: http://localhost:${port}`);
  console.log(`Data directory: ${dataDir}`);
  console.log(`Env file: ${envPath}`);

  const child = spawn(process.execPath, ["--import", "tsx", serverEntry], {
    cwd: packageRoot,
    env: {
      ...process.env,
      ...env,
      NODE_ENV: "production",
      DB_PATH: env.DB_PATH,
      SESSION_AUTH_TOKEN: env.SESSION_AUTH_TOKEN,
      REDIS_URL: env.REDIS_URL,
      REDIS_ENABLED: env.REDIS_ENABLED,
      PORT: String(port),
    },
    stdio: "inherit",
  });

  writeFileSync(pidPath, String(child.pid));

  const forward = (signal) => {
    if (!child.killed) child.kill(signal);
  };
  process.on("SIGINT", () => forward("SIGINT"));
  process.on("SIGTERM", () => forward("SIGTERM"));

  child.on("exit", (code, signal) => {
    rmSync(pidPath, { force: true });
    if (signal) process.kill(process.pid, signal);
    process.exit(code ?? 0);
  });
}

async function status() {
  const env = { ...parseEnvFile(envPath), ...process.env };
  const port = Number(env.PORT || "8791");
  const server = await health(port);
  console.log(`server: ${server ? `up http://localhost:${port}` : `down http://localhost:${port}`}`);

  const redisUrl = env.REDIS_URL || defaultRedisUrl;
  const target = redisTarget(redisUrl);
  const redis = await canConnect(target.host, target.port);
  console.log(`redis: ${redis ? "up" : "down"} ${redisUrl}`);
  console.log(`home: ${appHome}`);
}

function stop() {
  if (!existsSync(pidPath)) {
    console.log("No Agent Organizer pid file found.");
    return;
  }

  const pid = Number(readFileSync(pidPath, "utf8").trim());
  if (!pid) {
    rmSync(pidPath, { force: true });
    console.log("Removed invalid pid file.");
    return;
  }

  try {
    process.kill(pid, "SIGTERM");
    rmSync(pidPath, { force: true });
    console.log(`Stopped Agent Organizer process ${pid}.`);
  } catch {
    rmSync(pidPath, { force: true });
    console.log(`Process ${pid} was not running; removed pid file.`);
  }
}

async function doctor() {
  ensureNode();
  const env = parseEnvFile(envPath);
  const port = Number(env.PORT || process.env.PORT || "8791");
  console.log("Agent Organizer doctor");
  console.log(`node: ${process.version}`);
  console.log(`package: ${packageRoot}`);
  console.log(`home: ${appHome}`);
  console.log(`env: ${existsSync(envPath) ? envPath : "missing; start will create it"}`);
  console.log(`dist: ${existsSync(path.join(packageRoot, "dist", "index.html")) ? "ok" : "missing"}`);
  for (const command of ["git", "gh", "docker", "claude", "codex", "gemini"]) {
    console.log(`${command}: ${commandExists(command) ? "found" : "missing"}`);
  }
  const server = await health(port);
  console.log(`server: ${server ? `up http://localhost:${port}` : `down http://localhost:${port}`}`);
  const redisUrl = env.REDIS_URL || process.env.REDIS_URL || defaultRedisUrl;
  const target = redisTarget(redisUrl);
  console.log(`redis: ${(await canConnect(target.host, target.port)) ? "up" : "down"} ${redisUrl}`);
}

const command = process.argv[2] || "start";

try {
  if (command === "start") {
    await start();
  } else if (command === "status") {
    await status();
  } else if (command === "stop") {
    stop();
  } else if (command === "doctor") {
    await doctor();
  } else if (command === "--help" || command === "-h" || command === "help") {
    printHelp();
  } else {
    printHelp();
    process.exit(1);
  }
} catch (error) {
  fail(error instanceof Error ? error.message : String(error));
}
